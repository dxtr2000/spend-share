export type MessageKey = keyof typeof messages.hu

const initialLocale = (() => {
  if (typeof localStorage === 'undefined') return 'hu'
  const saved = localStorage.getItem(storageKey)
  return saved === 'en' || saved === 'hu' ? saved : 'hu'
})()

const locale = shallowRef<Locale>(initialLocale)

export function useI18n() {
  const currentLocale = computed(() => locale.value)

  function setLocale(nextLocale: Locale) {
    locale.value = nextLocale
    if (typeof localStorage !== 'undefined') localStorage.setItem(storageKey, nextLocale)
    if (typeof document !== 'undefined') document.documentElement.lang = nextLocale
  }

  function t(key: MessageKey, params: Record<string, string | number> = {}) {
    const template = messages[locale.value][key] ?? messages.en[key] ?? key
    return Object.entries(params).reduce(
      (text, [paramKey, value]) => text.split(`{${paramKey}}`).join(String(value)),
      String(template)
    )
  }

  return {
    locale: currentLocale,
    setLocale,
    t
  }
}

if (typeof document !== 'undefined') document.documentElement.lang = initialLocale
